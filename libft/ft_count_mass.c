#include "libft.h"

int	ft_count_mass(char const *s, char c)
{
	int i;

	i = 0;
	if (*s != c)
		i++;
	while (*s != '\0')
	{
		if (*s == c)
		{
			if (*(s + 1) != c && *(s + 1) != '\0')
				i++;
		}
		s++;
	}
	return (i);
}
